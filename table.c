void Permutation(int *In,int *Out,int n,int *P)//置换输入、输出、置换表长、置换表指针
{
    for(int i=0; i<n; i++){
        *(Out+i)=*(In+*(P+i)-1);//PC_1有h超过56的数字，这导致了溢出
    }
}

void XOR(int *In1,int *In2,int n,int *Out)//异或函数
{
    for(int i=0; i<n; i++)
        *(Out+i)=*(In1+i)^*(In2+i);//会自动把给定数字转化为二进制串再进行异或；
}

void shift(int *In,int *Out,int n,int s)//位移
{
    for(int i=0; i<n;i++)
        *(Out+i)=*(In+(i+s)%n);
}

void DToB(int c,int *result)//十进制转二进制，结果存到数组里
{
    for(int i=0;i<4;i++)
    {
        int m=1;
        *(result+3-i)=(c&(m<<i))>>i;
    }
}

void get_subkey(int *key,int subkey[17][48])//生成子秘钥
{
    int i=0,j=0;
    int C[17][28],D[17][28],temp1[56]={0};//17=1原始秘钥+16子秘钥
    for(i=0; i<64; i++){
     if((i+1)%8 !=0){
        *(temp1+j)=*(key+*(PC_1+i)-1);
        j++;
        }
     }//temp1存储着PC_1置换后的结果；
    for(i=0;i<56;i++)//分两段
    {
        if(i<28)
            C[0][i]=temp1[i];
        else
            D[0][i-28]=temp1[i];
    }
    for(i=1;i<=16;i++)
    {
        if(i==1 || i==2 || i==9 || i==16){
            shift(C[i-1],C[i],28,1);
            shift(D[i-1],D[i],28,1);
        }
        else{
            shift(C[i-1],C[i],28,2);
            shift(D[i-1],D[i],28,2);
            }

        for(j=0;j<56;j++)//更新temp1
        {
            if(j<28)
                temp1[j]=C[i][j];
            else
                temp1[j]=D[i][j-28];
        }
        Permutation(temp1,subkey[i],48,PC_2);//生成48位的子秘钥
    }
}

